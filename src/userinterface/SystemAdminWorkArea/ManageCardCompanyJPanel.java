    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
         int selectedRow = tblCardCompany.getSelectedRow();
        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(null,Organization.selectRow, "Warining", JOptionPane.WARNING_MESSAGE);
            return;
        }
        CardType cardComp = (CardType)tblCardCompany.getValueAt(selectedRow,0);
        UpdateCardTypeJPanel modifyCardComp = new UpdateCardTypeJPanel(userProcessContainer, ecoSystem, cardTypeList, cardComp);
        userProcessContainer.add("ModifyCardCompanyJPanel",modifyCardComp);
        CardLayout layout=(CardLayout)userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDelete1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelete1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblCardCompany.getSelectedRow();
        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(null,Organization.selectRow, "Warining", JOptionPane.WARNING_MESSAGE);
            return;
        }

        CardType cardComp = (CardType) tblCardCompany.getValueAt(selectedRow, 0);
        cardTypeList.removeCardCompany(cardComp);
        populateTable();
    }//GEN-LAST:event_btnDelete1ActionPerformed


