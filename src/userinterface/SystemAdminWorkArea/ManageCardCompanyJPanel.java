     public void populateTable() {
        DefaultTableModel cc = (DefaultTableModel) tblCardCompany.getModel();
        cc.setRowCount(0);
        if(ecoSystem.getCardTypeList().getCardTypeList() != null) {
           for(CardType cardCompany : cardTypeList.getCardTypeList()){
                Object [] row = new Object[5];
                row[0] = cardCompany;
                row[1] = cardCompany.getCode();
                row[2] = cardCompany.getCardPlatinumType();
                row[3] = cardCompany.getPointsEarnedPercent();
                row[4] = cardCompany.getLimit();
                cc.addRow(row);
            }            
        }
    }
