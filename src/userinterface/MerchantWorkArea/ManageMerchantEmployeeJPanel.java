    private void btnAddEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddEmployeeActionPerformed

        if(  name.getText().isEmpty() || user.getText().isEmpty() || pass.getText().isEmpty() ) {
            JOptionPane.showMessageDialog(null, "Fields cannot be left empty");
            return;
        }

        System.out.println(ecoSystem.toString());
        int empLength = ecoSystem.getMerchantEmployeeDirectory().getMerchantEmployeeDirectory().size();
        String empCode = "MEMP#"+empLength;
        ecoSystem.getMerchantEmployeeDirectory().newMerchantEmployee(name.getText(),addre.getText(), empCode, contact.getText(), merchant);
        Employee employee = ecoSystem.getEmployeeDirectory().createEmployee(empCode);
        UserAccount usserAccount = ecoSystem.getUserAccountDirectory().createUserAccount(user.getText(), pass.getText(), employee, new MerchantEmployeeRole());
        JOptionPane.showMessageDialog(null, "Merchant Employee Profile Created.");
        name.setText("");
        contact.setText("");
        addre.setText("");
        user.setText("");
        pass.setText("");
        populateTable();
    }//GEN-LAST:event_btnAddEmployeeActionPerformed

