    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblCustomer.getModel();
        dtm.setRowCount(0);
        for(MerchantEmployee be : ecoSystem.getMerchantEmployeeDirectory().getMerchantEmployeeDirectory()){
            if(merchant == be.getMerchant()){
            Object [] row = new Object[4];
            row[0] = be.getCode();
            row[1] = be.getName();
            row[2] = be.getAddress();
            row[3] = be.getPhoneNumber();
            dtm.addRow(row);
            }
        }
        }
