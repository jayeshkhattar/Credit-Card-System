        public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblCustomer.getModel();
        dtm.setRowCount(0);
        for(Bank bank : ecoSystem.getBankDirectory().getBankDirectory()){
            Object [] row = new Object[3];
            row[0] = bank;
            row[1] = bank.getCode();
            row[2] = bank.getMainBranchAdress();
            dtm.addRow(row);
        }
        }


